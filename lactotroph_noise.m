function [dx_, aux_]=lactotroph_noise(t,x_,w_,p_)
dx_=zeros(4,1);
aux_=zeros(2,1);
inoise=p_(3)*w_(1);
c2=x_(4)*x_(4);
minf=(1)/((1)+exp(((-20)-x_(1))/(12)));
ninf=(1)/((1)+exp(((-5)-x_(1))/(10)));
finf=(1)/((1)+exp(((-20)-x_(1))/(2)));
ica=(1.5)*minf*(x_(1)-(60));
vkdrive=x_(1)-(-75);
isk=(3)*c2/(c2+(0.4)*(0.4))*vkdrive;
ibk=p_(1)*x_(3)*vkdrive;
ik=(2)*x_(2)*vkdrive;
il=(0.05)*(x_(1)-(-50));
itot=ica+isk+ibk+ik+il+inoise;
dx_(1,1)=-itot/(10);
dx_(2,1)=(ninf-x_(2))/(30);
dx_(3,1)=(finf-x_(3))/p_(2);
dx_(4,1)=-(0.01)*((0.0015)*ica+(0.2)*x_(4));
aux_(1,1)=ica;
aux_(2,1)=inoise;
